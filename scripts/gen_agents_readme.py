"""Auto-generate ~/.claude-agents-v2/agents/README.md from YAML frontmatter of all keep-list cartridges."""
from pathlib import Path
import re
from datetime import date

STAGING = Path.home() / ".claude" / "agents"  # post-Wave 5.4: reads from live path
KEEP = [
    "architect",
    "llmops-expert",
    "backend-expert",
    "frontend-expert",
    "devops-expert",
    "adversarial",
    "validate",
    "researcher",
    "scraper",
    "drafter",
    "prompt-engineer",
    "eval-writer",
    "sme-reviewer",
]

CODEX_MODE = {
    "architect": "skip",
    "llmops-expert": "blocking",
    "backend-expert": "concurrent",
    "frontend-expert": "concurrent",
    "devops-expert": "blocking",
    "adversarial": "concurrent",
    "validate": "skip",
    "researcher": "skip",
    "scraper": "concurrent",
    "drafter": "concurrent",
    "prompt-engineer": "concurrent",
    "eval-writer": "concurrent",
    "sme-reviewer": "skip",
}

WAVE = {
    "architect": "2 (Task 2.5)",
    "llmops-expert": "2 (Task 2.1, absorbs integrator)",
    "backend-expert": "2 (Task 2.2)",
    "frontend-expert": "2 (Task 2.3, absorbs jsdoc)",
    "devops-expert": "2 (Task 2.4)",
    "adversarial": "3 (Task 3.1, absorbs security-reviewer + analyst)",
    "validate": "3 (Task 3.2)",
    "researcher": "3 (Task 3.3)",
    "scraper": "3 (Task 3.3)",
    "drafter": "3 (Task 3.6, SDD fallback)",
    "prompt-engineer": "3 (Task 3.4, NEW)",
    "eval-writer": "3 (Task 3.5, NEW)",
    "sme-reviewer": "3 (Task 3.5, NEW)",
}


def parse_frontmatter(path: Path) -> dict:
    text = path.read_text(encoding="utf-8")
    m = re.match(r"^---\s*\n(.*?)\n---\s*\n", text, re.DOTALL)
    if not m:
        raise ValueError(f"no frontmatter in {path}")
    fields = {}
    for line in m.group(1).splitlines():
        if ":" in line:
            k, _, v = line.partition(":")
            fields[k.strip()] = v.strip()
    lines = len(text.splitlines())
    return {**fields, "lines": lines}


def main():
    rows = []
    for name in KEEP:
        path = STAGING / f"{name}.md"
        if not path.exists():
            raise FileNotFoundError(path)
        fm = parse_frontmatter(path)
        model_short = fm["model"].replace("claude-", "").split("-2")[0]
        if "haiku" in model_short:
            model_short = "haiku-4-5"
        elif "sonnet" in model_short:
            model_short = "sonnet-4-6"
        rows.append(
            {
                "name": name,
                "model": model_short,
                "maxTurns": fm.get("maxTurns", "?"),
                "codex": CODEX_MODE[name],
                "wave": WAVE[name],
                "lines": fm["lines"],
                "description": fm.get("description", ""),
            }
        )

    today = date.today().isoformat()
    out = []
    out.append("# Group of Experts — Cartridge v2 Roster")
    out.append("")
    out.append(f"**Auto-generated** {today} by `scripts/gen_agents_readme.py` from YAML frontmatter of `~/.claude/agents/*.md` (live path, post-Wave 5.4 swap).")
    out.append("")
    out.append("Do not edit this file by hand — regenerate after any cartridge frontmatter change.")
    out.append("")
    out.append("## Roster (13 experts)")
    out.append("")
    out.append("| Agent | Model | maxTurns | Codex mode | Lines | Wave |")
    out.append("|-------|-------|----------|-----------|-------|------|")
    for r in rows:
        out.append(
            f"| **{r['name']}** | {r['model']} | {r['maxTurns']} | codex-{r['codex']} | {r['lines']} | {r['wave']} |"
        )
    out.append("")
    out.append("## Descriptions")
    out.append("")
    for r in rows:
        out.append(f"### {r['name']}")
        out.append("")
        out.append(r["description"])
        out.append("")
    out.append("## Cartridge structure (10 slots)")
    out.append("")
    out.append("Every cartridge follows the shared template documented in `docs/superpowers/specs/agent-cartridge-v2.md`:")
    out.append("")
    out.append("1. ROLE — distinctive expertise (2-3 lines)")
    out.append("2. HYDRATION PROTOCOL — files to read before responding")
    out.append("3. TRIGGER HEURISTICS — when this agent owns the task")
    out.append("4. DOMAIN PATTERNS — canonical code snippets for the domain")
    out.append("5. HANDOFF CONTRACT — task-brief input + return-schema output")
    out.append("6. REVIEW CONTRACT — codex_mode (blocking / concurrent / skip)")
    out.append("7. SELF-CRITIQUE CHECKLIST — 3-5 questions before returning")
    out.append("8. ESCALATION TRIGGERS — when to hand off to a different expert")
    out.append("9. WHAT YOU DO NOT DO — hard scope boundaries")
    out.append("10. COST BUDGET — advisory ceiling (only maxTurns is a hard stop)")
    out.append("")
    out.append("## Codex mode taxonomy")
    out.append("")
    blocking = [r["name"] for r in rows if r["codex"] == "blocking"]
    concurrent = [r["name"] for r in rows if r["codex"] == "concurrent"]
    skip = [r["name"] for r in rows if r["codex"] == "skip"]
    out.append(f"- **codex-blocking** ({len(blocking)}): {', '.join(blocking)} — Codex findings gate the merge.")
    out.append(f"- **codex-concurrent** ({len(concurrent)}): {', '.join(concurrent)} — Codex runs in parallel; findings folded into task review.")
    out.append(f"- **codex-skip** ({len(skip)}): {', '.join(skip)} — Codex not required (no code, orchestration only, or final gate).")
    out.append("")
    out.append("## Retired agents (archived at Wave 5.4)")
    out.append("")
    out.append("- `analyst` → merged into `adversarial` Slot 4 (read-only diagnostics mode)")
    out.append("- `security-reviewer` → merged into `adversarial` Slot 4 (OWASP + secrets scan)")
    out.append("- `jsdoc` → merged into `frontend-expert` Slot 4 (TSDoc emission pattern)")
    out.append("- `integrator` → merged into `llmops-expert` Slot 4 (orchestrator.py wiring pattern)")
    out.append("- `lain-specialist` → retired per memory feedback (never use)")
    out.append("")

    readme = STAGING / "README.md"
    readme.write_text("\n".join(out), encoding="utf-8")
    print(f"wrote {readme} ({len(out)} lines)")


if __name__ == "__main__":
    main()
